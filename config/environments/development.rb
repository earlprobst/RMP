RemoteManagementPlatform::Application.configure do
  # Settings specified here will take precedence over those in config/environment.rb

  # auto rotate log files, keep 2 of 50MB each
  Dir.mkdir("#{::Rails.root.to_s}/log") unless File.directory?("#{::Rails.root.to_s}/log")
  config.logger = Logger.new(config.paths.log.first, 2, 50.megabytes)
  
  # In the development environment your application's code is reloaded on
  # every request.  This slows down response time but is perfect for development
  # since you don't have to restart the webserver when you make code changes.
  config.cache_classes = false

  # Log error messages when you accidentally call methods on nil.
  config.whiny_nils = true

  # Show full error reports and disable caching
  config.consider_all_requests_local       = true
  config.action_view.debug_rjs             = true
  config.action_controller.perform_caching = false

  # Don't care if the mailer can't send
  config.action_mailer.raise_delivery_errors = false

  # Print deprecation notices to the Rails logger
  config.active_support.deprecation = :log

  # Only use best-standards-support built into browsers
  config.action_dispatch.best_standards_support = :builtin
end

